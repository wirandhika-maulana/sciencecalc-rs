/// Modul Gaya
/// Force Module

/// Menghitung gaya (F) dari massa (m) dan percepatan (a).
/// Calculates force (F) from mass (m) and acceleration (a).
/// Rumus: F = m × a
/// Formula: F = m × a
pub fn gaya(m: f64, a: f64) -> f64 {
    // F = gaya (Newton), m = massa (kg), a = percepatan (m/s^2)
    // F = force (Newton), m = mass (kg), a = acceleration (m/s^2)
    m * a
}
